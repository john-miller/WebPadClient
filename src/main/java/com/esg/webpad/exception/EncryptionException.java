package com.esg.webpad.exception;

public class EncryptionException extends Exception {

	private static final long serialVersionUID = 1088255114622837719L;

	public EncryptionException() {
		super();
	}

	public EncryptionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public EncryptionException(String arg0) {
		super(arg0);
	}

	public EncryptionException(Throwable arg0) {
		super(arg0);
	}

}
