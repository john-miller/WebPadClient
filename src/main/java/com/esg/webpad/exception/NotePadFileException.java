package com.esg.webpad.exception;

public class NotePadFileException extends Exception {

	private static final long serialVersionUID = 1088255114622837719L;

	public NotePadFileException() {
		super();
	}

	public NotePadFileException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NotePadFileException(String arg0) {
		super(arg0);
	}

	public NotePadFileException(Throwable arg0) {
		super(arg0);
	}

}
