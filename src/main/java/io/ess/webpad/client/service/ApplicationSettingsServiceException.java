package io.ess.webpad.client.service;

public class ApplicationSettingsServiceException extends Exception {

	private static final long serialVersionUID = 7775124741000204709L;

	public ApplicationSettingsServiceException() {
		super();
	}

	public ApplicationSettingsServiceException(String message, Throwable throwable) {
		super(message, throwable);
	}

	public ApplicationSettingsServiceException(String message) {
		super(message);
	}

	public ApplicationSettingsServiceException(Throwable throwable) {
		super(throwable);
	}
	
	

}
